quote = 'Knowledge is power. Power is gained through knowledge.'
f=quote.find('power')
print(f)

rf=quote.rfind('knowledge')
print(rf)

tit=quote.title()
print(tit)

up=quote.upper()
print(up)

ss=quote.split(' ')
print(ss)

par=quote.partition('is')
print(par)

alp=quote.isalpha()
print(alp)


